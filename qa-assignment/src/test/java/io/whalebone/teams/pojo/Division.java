package io.whalebone.teams.pojo;

public class Division {

    private int id;
    private String name;

    public Division() {
    }
    
    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
